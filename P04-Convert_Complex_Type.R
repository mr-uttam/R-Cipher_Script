num1 <- as.numeric(76 + 4i)
print("Converted complex to numerical:")
print(num1)

# Converting complex to character
num2 <- as.character(76 + 4i)
print("Converted complex to character:")
print(num2)

# Converting complex to logical
num3 <- as.logical(76 + 4i)
print("Converted complex to logical:")
print(num3)

# Converting complex to integer
num4 <- as.integer(76 + 4i)
print("Converted complex to integer:")
print(num4)
