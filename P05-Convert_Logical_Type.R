num1 <- as.numeric(TRUE)
print("Converted logical to numerical:")
print(num1)

# Converting logical to character
num2 <- as.character(TRUE)
print("Converted logical to character:")
print(num2)

# Converting logical to integer
num3 <- as.integer(TRUE)
print("Converted logical to integer:")
print(num3)

# Converting logical to complex
num4 <- as.complex(TRUE)
print("Converted logical to complex:")
print(num4)
